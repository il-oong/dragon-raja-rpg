  function pickRaceArt(race) {
    if (race === 'elf') return ['elf', 'elf'];
    if (race === 'dwarf') return ['dwarf', 'dwarf'];
    return ['hero', 'hero'];
  }

  function monsterArt(key) {
    const k = String(key || '').toLowerCase();
    if (k.includes('dragon') || k.includes('wyrm') || k.includes('wyvern') || k.includes('amurtat')) return ['dragon', 'dragon'];
    if (k.includes('wolf') || k.includes('hound')) return ['wolf', k.includes('ice') ? 'ice' : 'wolf'];
    if (k.includes('spider')) return ['spider', 'spider'];
    if (k.includes('slime')) return ['slime', 'water'];
    if (k.includes('zombie') || k.includes('skeleton') || k.includes('wraith') || k.includes('lich')) return ['undead', 'undead'];
    if (k.includes('fire') || k.includes('imp') || k.includes('demon')) return ['elemental', 'fire'];
    if (k.includes('ice') || k.includes('frost')) return ['elemental', 'ice'];
    return ['goblin', 'monster'];
  }

  function itemArt(key, item) {
    const k = String(key || '').toLowerCase();
    const type = item && item.type;
    if (type === 'use' || k.includes('potion') || k.includes('ether') || k.includes('elixir')) return ['potion', 'item'];
    if (type === 'weapon' || k.includes('sword') || k.includes('axe') || k.includes('bow') || k.includes('staff')) return ['sword', 'item'];
    if (type === 'armor') return ['armor', 'stone'];
    if (type === 'acc' || k.includes('ring') || k.includes('amulet')) return ['ring', 'item'];
    if (type === 'skillbook' || k.includes('book')) return ['armor', 'water'];
    return ['ring', 'item'];
  }

  function locationArt(key) {
    const k = String(key || '').toLowerCase();
    if (k.includes('forest') || k.includes('grove') || k.includes('elf')) return ['forest', 'scene'];
    if (k.includes('mount') || k.includes('dragon')) return ['mountain', 'stone'];
    if (k.includes('mine') || k.includes('cave') || k.includes('dungeon')) return ['cave', 'stone'];
    if (k.includes('castle') || k.includes('palace') || k.includes('capital') || k.includes('tower')) return ['castle', 'stone'];
    return ['town', 'scene'];
  }

  function rects(pattern, palette) {
    const px = 4;
    return pattern.map((row, y) => row.split('').map((c, x) => {
      const fill = palette[Number(c)] || palette[0];
      if (fill === 'transparent') return '';
      return `<rect x="${x * px}" y="${y * px}" width="${px}" height="${px}" fill="${fill}"/>`;
    }).join('')).join('');
  }

  function svgByPattern(patternName, paletteName, opts = {}) {
    const pattern = art[patternName] || art.hero;
    const palette = palettes[paletteName] || palettes.hero;
    const size = opts.size || 64;
    const title = String(opts.title || patternName);
    return `<svg class="px-art ${opts.className || ''}" role="img" aria-label="${escapeHtml(title)}" width="${size}" height="${size}" viewBox="0 0 64 64" shape-rendering="crispEdges">${rects(pattern, palette)}</svg>`;
  }

  function escapeHtml(s) {
    return String(s == null ? '' : s).replace(/[&<>"']/g, c => ({
      '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;',
    }[c]));
  }

  function render(type, key, meta = {}, opts = {}) {
    let pair;
    if (type === 'player') pair = pickRaceArt(key);
    else if (type === 'monster') pair = monsterArt(key);
    else if (type === 'item') pair = itemArt(key, meta);
    else if (type === 'location') pair = locationArt(key);
    else pair = ['hero', 'hero'];
    return svgByPattern(pair[0], pair[1], { ...opts, title: opts.title || key || type });
  }

  return { render, svgByPattern };
